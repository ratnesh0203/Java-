public class operator {
    public static void main(String[] args) {
        System.out.println(2&3); //bitwise and
        System.out.println(2|3); //bitwise or
        System.out.println(2^3); //bitwise ex-or
        System.out.println(~2);  //bitwise compliment
        System.out.println(34<<1);  //bitwise Left shift
        System.out.println(34>>2);  //bitwise Right shift
        System.out.println(-4<<1);  
        System.out.println(-4>>1);  
                

    }
}
